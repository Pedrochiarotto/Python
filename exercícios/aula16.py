#Introdução aos blocos de código -> IF//elif// else(condicionais)
#If  //elif  // else  
#Se // Se não se//  Se não

entrada = input('Você quer "entrar" ou "sair"? ')

if entrada == 'entrar':
    print("Você entrou!")
elif entrada == 'sair':
    print("Você saiu!")
elif entrada == 'Entrar':
    print("Você entrou!!")
elif entrada == 'Sair':
    print("Você Saiu!!")
else: 
    print("Resposta inválida!")